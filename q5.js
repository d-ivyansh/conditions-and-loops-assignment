//for printing in ascending order
console.log("for printing in ascending order");
let num = 1;
while(num<=25)
{
    console.log(num);
    num++;
}
//for printing in descending order
console.log("for printing in descending order"); 
let num2 = 25;
while(num2>=1)
{
    console.log(num2);
    --num2;
}